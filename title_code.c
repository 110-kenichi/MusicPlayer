void processTitle(char vblank) {
  if (vblank) {
    switch (PhaseCounter) {
      case 0: {
        DisableVDPProcessing = true;
        SMS_displayOff();
        InitGameVars();

        SMS_loadPSGaidencompressedTiles(title_bg_tile_psgcompr, 0);
        SMS_loadTileMapArea(0, 0, title_bg_map_bin, 32, 24);

        // SMS_setNmiInterruptHandler(NmiInterruptHandler);
        PSGPlay(aleste_vgm_psg);
    
        DisableVDPProcessing = false;
        PhaseCounter++;
        SMS_displayOn();
        break;
      }
      case 1: {
        FadeInPalette(PhaseLocalCounter, title_bg_pal_bin,
                      title_bg_pal_bin_size, 0);
        PhaseLocalCounter++;
        if (PhaseLocalCounter == 7) {
          PhaseCounter++;
          PhaseLocalCounter = 0;
        }
        break;
      }
      case 2: {
        if (SMS_getKeysPressed() == PORT_A_KEY_2) {
          PhaseLocalCounter = 0;
          PhaseCounter++;
          break;
        }

        break;
      }
      case 3: {
        if (PhaseLocalCounter > 6) {
          PSGStop();
          GamePhase = G_PHASE_SEL;
        } else {
          FadeOutPalette(PhaseLocalCounter, title_bg_pal_bin,
                         title_bg_pal_bin_size, 0);
        }
        PhaseLocalCounter++;
        break;
      }
    }
  }
}
